def mergevalues(first, second, merge_conflicts="%s|%s"):
    """return only value if first == second, or their merged string

    `mergevalues` es una función de tres parámetros, los primeros dos
    son los valores a juntar, el tercero es el formato en que se
    combinan los dos valores en el caso de conflicto. la función
    averigua si los dos primeros argumentos tienen el mismo valor, en el
    cual caso, este será el valor de la función. si los dos primeros
    argumentos tienen valor diferentes, en tercer argumento es utilizado
    para combinarlos.

    """

    if first is None:
        first = ''
    if second is None:
        second = ''
    if str(first) == str(second):
        return str(first)
    if not first:
        return str(second)
    if not second:
        return str(first)
    return merge_conflicts % (first, second)
